package com.pcxserver.markovChains;

public class FrequencyPairs {

    String first;
    int    second;

    public FrequencyPairs(String first, int second) {
        ;
        this.first = first;
        this.second = second;
    }

    public String getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }

    public void setFirst(String first) {
        this.first = first;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
